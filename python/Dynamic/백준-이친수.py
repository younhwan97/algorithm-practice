import sys

n = int(sys.stdin.readline())

dp = [0] * 91

dp[1] = 1
dp[2] = 1

for i in range(3, len(dp)):
    ## dp[1] = 1 = 1

    ## dp[2] = 10 = 1

    ## dp[3] = 101
    ##         100
    ##         1 * 1 * 2 = 2

    ## dp[4] = 1010
    ##         1001
    ##         1000
    ##         1 * 1 * 2 * 2 - 1 = 3

    ## dp[5] = 10101
    ##         10100
    ##         10010
    ##         10001
    ##         10000
    ##         1 * 1 * 2 * 2 * 2 - 3 = 5
    
    dp[i] = 2 ** (i - 2) 

print(dp[n])